function TabIcon({ xml, color }: { xml: string; color: string }) {
  const colored = xml.replace(/FILL_COLOR/g, color);
  return <SvgXml xml={colored} />;
}

export default function TabBar() {
  const router = useRouter();
  const pathname = usePathname();

  return (
    <View style={styles.container}>
      {TABS.map((tab) => {
        const isActive = pathname === tab.route;
        return (
          <TouchableOpacity
            key={tab.route}
            style={styles.tab}
            onPress={() => router.push(tab.route as any)}
          >
            <TabIcon
              xml={tab.icon}
              color={isActive ? "#ffffff" : "rgba(255,255,255,0.5)"}
            />
          </TouchableOpacity>
        );
      })}
    </View>
  );
}

